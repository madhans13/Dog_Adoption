interface AppSidebarProps extends React.ComponentProps<typeof Sidebar> {
  data?: {
    user: {
      name: string
      email: string
      avatar: string
    }
    teams: {
      name: string
      logo: React.ElementType
      plan: string
    }[]
    navMain: {
      title: string
      url: string
      icon?: React.ElementType
      isActive?: boolean
      items?: {
        title: string
        url: string
      }[]
    }[]
    projects: {
      name: string
      url: string
      icon: React.ElementType
    }[]
  }
  onLogout?: () => void
  onNavigate?: (section: string) => void
  activeSection?: string
}

export function AppSidebar({ data: customData, onLogout, onNavigate, activeSection, ...props }: AppSidebarProps) {
  const sidebarData = customData || data
  
  return (
    <Sidebar collapsible="icon" {...props}>
      <SidebarHeader>
        <TeamSwitcher teams={sidebarData.teams} />
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={sidebarData.navMain} onNavigate={onNavigate} activeSection={activeSection} />
        <NavProjects projects={sidebarData.projects} onNavigate={onNavigate} activeSection={activeSection} />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={sidebarData.user} onLogout={onLogout} />
      </SidebarFooter>
      <SidebarRail />
    </Sidebar>
  )
}
